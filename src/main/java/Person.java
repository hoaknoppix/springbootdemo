/**
 * This is the object for automatic parsing demo by responsebody annotation
 * @author hoakn
 *
 */
public class Person {
	
	private int index;
	private String name;
	
	public int getIndex() {
		return index;
	}
	
	public void setIndex(int index) {
		this.index = index;
	}
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	

}
